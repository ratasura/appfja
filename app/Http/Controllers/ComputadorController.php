     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        if ($request) {
            $query = trim($request->get('searchText'));
            $computadoras = DB::table('computadors')->where('tipo','=','PC')
            ->where('responsable','LIKE','%'.$query.'%')
            ->orWhere('caf','LIKE','%'.$query.'%')
            ->orderby('id','desc')
            ->paginate(10);
            return view ('computadoras.index', ['computadoras'=>$computadoras, "searchText" => $query]);
        }
    }

